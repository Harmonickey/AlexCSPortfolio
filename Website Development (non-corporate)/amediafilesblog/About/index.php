                    <h2>Some more about me:</h2>
                	<div id="about">
                        <p>I do a bunch of various stuff, working on projects, going out, gaming. Pretty much all of it includes a mass-marathon or media. So naturally one day, I decided to see if I could maybe monetize it, at least enough to support the blog I decided to make. Here it is, the offspring of a few friends, in all its glory. I post reviews of the stuff I ingest. I hope to do it all justice. Some things don't deserve a good review, they're just that bad. Some do. It just depends on how much I like it.</p>
                        </br>
                        <p>So what do I like? I like stuff that makes me laugh, stuff that has decent explosions(though, any large explosion pretty much satisfies), stuff that has an exceptional plot. These are just a few things.</p>
                        <p>I approve of a good action movie, but it can't be too serious. There's a reason why I liked The Expendables 2. Explosions and puns for the sake of explosions and puns. I made me laugh. Very good stuff. A good story intrigues me aswell.</p>
                        <p>Then comes the field of stuff I don't like. Stuff I classify as "Horror" and "American Comedy". The former involves stuff such as The Hangover. The later something like Saw. Don't get me wrong, I liked Disturbia. But not because it was scary. It was rediculous. It made me laugh at how horrible the scare tactics were. That's why I liked it.</p>
                        <p>Then again, a good comedy make me very happy. But, I prefer comedies from the United Kingdom. Something like Spaced or Green Wing. Both are rediculous, but not like The Hangover. They don't give me the feeling like they're trying too hard, because that's what The Hangover makes me feel like.</p>
                        </br>
                        <p>Anyways, almost every movie makes me happy. From Australian, to Bollywood, to German, English, or Hollywood. Even extremily cheap third party stuff floats my boat.</p>
                        </br>
                        </br>
                        <p>In terms of T.V. Shows, those go the same way. Podcasts, though, are a little different. I watch a lot of Hacking/Electronics based shows. I also have a lot of music podcasts. I classify youtube videos as podcasts too. In that regard, I have a few parkour channels, Formula 1 team channels, and comedy channels. But, the biggest part of my youtube subscriptions are made up of video production people. People who go out and make videos, and then edit the crap out of them. These are glorious and I love them.</p>
                        </br>
                        </br>
                        <p>Music, that's a hard topic to talk about. I have tastes ranging from Rachmaninov to Hardstyle(Hard House). From big band soundtracks to Iron Maiden. My tastes are ADHD, as I like to say. Most of the time, I focus on Techno and House. Things with a good drum and bass track make me happy. Notably, The Blue Man Group. Then again, Bach and Mozart floats my boat too.</p>
                        <p>I hate some genres though. Country, Metal, and Rap, most notably. Don't get me wrong, Country is great. For a blue-collar working job. It's good for working. Metal just make me angry and want to hurt things. I can't stand it. Rap is the worst. I believe in speaking properly, and Rap artists don't.</p>
                        <p>Of course, there are a few exceptions to all of these. My group of friends normally focusses on a bad song, and it becomes a running joke. At this time, it's Macklemore's Thrift Shop. It's so bad it's actually good.</p>
                        </br>
                        </br>
                        <p>That's Me and my tastes, in a nutshell. I also run my personal blog, where I also host my personal portfolio and stuff <a href="www.saurutobi.com">here.</a></p>
